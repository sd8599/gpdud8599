package javarlch;
//주어진 코드를 분석하여라
public class Multiply {
	public static void times(double a,double b) {
		//자동형변환 a = 2.0, b = 2.0
		
		System.out.println(a*b);
	}	
	//프로그램의 시작점
	public static void main(String[] args) {
			
		times(2,2);//호출,메소드로 값을 던지고있음
		
		times(3,4);//호출,메소드로 값을 던지고있음
		}
}
//4.0
