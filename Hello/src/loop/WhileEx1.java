/*자연수 출력
 * 처음 10개의 자연수를 출력하는 프로그램을 작성해 보세요. 
 * 이때, count 라는 변수를 사용해야 됩니다.
 * while문, for문 2가지 버전의 정답을 만드세요.
 */
public class WhileEx1 {
		public static void main(String[] args) {
			
			int count = 1;
			 
			 while (count<=10) { //count++;도가능
				System.out.println(count);
				count++;
			}
		}
}
