package loop;
/*간단한 예제를 통해 반복문이 왜 필요한지 이유를 알아보자.
 * 1을 한번씩 더해서 총 3번 더하는 반복문을 제공한다.
 */
public class While1_1 {
		public static void main(String[] args) {
				
				int count = 0;
				
				count = count+1;
				System.out.println("현재 숫자는 : "+count);

				count = count+1;
				System.out.println("현재 숫자는 : "+count);
				
				count = count+1;
				System.out.println("현재 숫자는 : "+count);
		}
}
